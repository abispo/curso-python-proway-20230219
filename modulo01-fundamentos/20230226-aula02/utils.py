from random import randint

if __name__ == "__main__":
    
    # A função randint(a, b) retorna um número randômico entre o intervalo (a, b)
    # Gera um número randômico entre 1 e 10
    print(randint(1, 10))

    # Utilizando o laço for para gerar uma sequência de 1 a 100
    for numero in range(1, 101):
        print(numero)
